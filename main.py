from pet import Pet

# Create a pet object
my_pet = Pet("Buddy")  # Replace "Buddy" with your pet's name

# Interact with the pet
my_pet.get_status()
my_pet.eat()
my_pet.play()
my_pet.sleep()
my_pet.get_status()

# Teach the pet tricks
my_pet.train("Sit")
my_pet.train("Roll Over")
my_pet.show_tricks()

# Final status
my_pet.get_status()
